// Function to filter locations based on search query
export const filterLocations = (query) => {
  if (!query || query.trim() === '') {
    return [];
  }
  const lowerQuery = query.toLowerCase().trim();
  return locations.filter(location => 
    location.toLowerCase().includes(lowerQuery)
  ).slice(0, 10); // Limit to 10 results
};

// Function to get all locations (for dropdown)
export const getAllLocations = () => {
  return locations.sort(); // Return sorted list
};

